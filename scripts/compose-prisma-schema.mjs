import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";

const engine = process.argv[2] ?? "sqlite";
const outputPath = process.argv[3] ?? "prisma/schema.sqlite.prisma";

const validEngines = new Set(["sqlite", "postgres"]);
if (!validEngines.has(engine)) {
  throw new Error(`Unsupported engine: ${engine}`);
}

const header = await readFile(
  join("prisma", "engines", engine, "header.prisma"),
  "utf8",
);
const models = await readFile(join("prisma", "main", "models.prisma"), "utf8");

const banner = `// This file is generated by scripts/compose-prisma-schema.mjs\n// Engine: ${engine}\n\n`;
const composed = `${banner}${header.trim()}\n\n${models.trim()}\n`;

await mkdir(dirname(outputPath), { recursive: true });
await writeFile(outputPath, composed, "utf8");

console.log(`Composed Prisma schema for ${engine} at ${outputPath}`);
