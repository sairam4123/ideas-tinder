import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { pathToFileURL } from "node:url";

const validEngines = new Set(["sqlite", "postgres"]);

export const resolvePrismaEngine = (engineOverride) => {
  const resolvedEngine =
    engineOverride ??
    (process.env.NODE_ENV === "production" ? "postgres" : "sqlite");

  if (!validEngines.has(resolvedEngine)) {
    throw new Error(`Unsupported engine: ${resolvedEngine}`);
  }

  return resolvedEngine;
};

export const composePrismaSchema = async ({
  engine: engineOverride,
  outputPath = join("prisma", "schema.prisma"),
} = {}) => {
  const engine = resolvePrismaEngine(engineOverride);
  const header = await readFile(
    join("prisma", "engines", engine, "header.prisma"),
    "utf8",
  );
  const models = await readFile(
    join("prisma", "main", "models.prisma"),
    "utf8",
  );

  const banner = `// This file is generated by scripts/compose-prisma-schema.mjs\n// Engine: ${engine}\n\n`;
  const composed = `${banner}${header.trim()}\n\n${models.trim()}\n`;

  await mkdir(dirname(outputPath), { recursive: true });
  await writeFile(outputPath, composed, "utf8");

  return { engine, outputPath };
};

const isCliEntryPoint =
  process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href;

if (isCliEntryPoint) {
  const { engine, outputPath } = await composePrismaSchema({
    engine: process.argv[2],
    outputPath: process.argv[3] ?? join("prisma", "schema.prisma"),
  });

  console.log(`Composed Prisma schema for ${engine} at ${outputPath}`);
}
